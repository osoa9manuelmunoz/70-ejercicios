n = int(input("ingrese el numero que lo quieres pasar a letra del 1 al 10: "))

if n==1:
    print("{}--uno".format(n))
elif n==2:
    print("{}--dos".format(n))
elif n==3:
    print("{}--tres".format(n))
elif n==4:
    print("{}--cuatro".format(n))
elif n==5:
    print("{}--cinco".format(n))
elif n==6:
    print("{}--seis".format(n))
elif n==7:
    print("{}--siete".format(n))
elif n==8:
    print("{}--ocho".format(n))
elif n==9:
    print("{}--nueve".format(n))
elif n==10:
    print("{}--dies".format(n))
else:
    print("no ingresaste un numero valido entre 1 al 10")
